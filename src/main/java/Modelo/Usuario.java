package Modelo;

/**
 *
 * @author MI PC
 */
public class Usuario {
    private String nombre;
    private String email;
    private String contraseña;
    private String tipoUsuario;

    public Usuario(String nombre, String email, String contraseña, String tipoUsuario) {
        this.nombre = nombre;
        this.email = email;
        this.contraseña = contraseña;
        this.tipoUsuario = tipoUsuario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getTipoUsuario() {
        return tipoUsuario;
    }

   
}
